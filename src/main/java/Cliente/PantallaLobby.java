    private void btnSendPrivateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendPrivateActionPerformed
        String texto = txtDetalle.getText().trim();
        String receptor = comboReceptores.getSelectedItem().toString();
        
        if (!texto.isBlank()){
            try {
                TipoMensaje tipo = receptor.equals("ALL") ? TipoMensaje.PUBLICO : TipoMensaje.PRIVADO;
                Mensaje mensaje = new Mensaje(nombreJugador, texto, receptor, tipo);
                salida.writeObject(mensaje);
                 if (tipo == TipoMensaje.PRIVADO) {
                    areaChat.append("[Privado a " + receptor + "] " + texto + "\n");
                }

                txtDetalle.setText(""); 
            } catch (IOException e) {
                areaChat.append("[Error] No se pudo enviar el mensaje\n");
            }
        }
        
    }//GEN-LAST:event_btnSendPrivateActionPerformed

    private void btnPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPartidaActionPerformed
        if (!puedeIngresar) {
            JOptionPane.showMessageDialog(this, "Ya saliste de la partida. Esperá a que se reinicie para volver a unirte.");
            return;
        }
        ZonaJuego zona = new ZonaJuego();
        zona.initData(nombreJugador, salida, entrada, nombreMapa, escucha);
        
        escucha.setReceptor(zona);
        escucha.reenviarJugadores();

        zona.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnPartidaActionPerformed

